char	*rush00(int x, int y, char *str)
{
	int	l;
	int i;
	int c;

	if (x <= 0 || y <= 0)
		return ((void*)0);
	l = 0;
	i = 0;
	while (l < y)
	{
		c = 0;
		while (c < x)
		{
			if ((c == 0 || c == x - 1) && (l == 0 || l == y - 1))
				str[i] = 'o';
			else if (l == 0 || l == y - 1)
				str[i] = '-';
			else if (c == 0 || c == x - 1)
				str[i] = '|';
			else
				str[i] = ' ';
			c++;
			i++;
		}
		str[i] = '\n';
		l++;
		i++;
	}
	str[i] = '\0';
	return (str);
}
